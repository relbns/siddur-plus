import { useContextStore } from '../../core/stores';

export interface HalachaEntry {
  id: string;
  title: string;
  summary: string;
  contentHe: string;
  priority: 1 | 2 | 3;
}

export function useHalachaFeed(): HalachaEntry[] {
  const context = useContextStore((s) => s.context);
  const items: HalachaEntry[] = [];

  if (!context) return [];

  if (context.isPesach || context.isCholHamoed) {
    items.push({
      id: 'pesach-chametz',
      title: 'איסור חמץ',
      summary: 'בחג הפסח ובחול המועד אסור לאכול ולהחזיק חמץ. יש להקפיד על מאכלים כשרים לפסח בלבד.',
      contentHe: 'איסור חמץ בפסח הוא מן התורה, שנאמר "שבעת ימים שאור לא ימצא בבתיכם". האיסור חל בכל תקופת החג ובחול המועד. בנוסף לאיסור אכילה, יש איסור הנאה מן החמץ וכן איסור "בל יראה ובל ימצא" המחייב ביעור או מכירת החמץ קודם התקדש החג. כל מאכל חייב בהשגחה מיוחדת לפסח כדי לוודא שלא התערב בו חמץ.',
      priority: 1,
    });
  }

  if (!context.tefillinAllowed) {
    items.push({
      id: 'no-tefillin',
      title: 'לא מניחים תפילין',
      summary: context.isShabbat
        ? 'בשבת קודש אין מניחים תפילין.'
        : context.isCholHamoed
        ? 'בחול המועד לא מניחים תפילין (ע"פ מנהג הפושט).'
        : 'ביום טוב אין מניחים תפילין.',
      contentHe: 'ימי שבת ויום טוב נקראים "אות", והתפילין גם הם "אות". לכן, העטור בתפילין בשבת וביום טוב נחשב זלזול באות של היום הקדוש, ומשום כך פטרו חכמים ולימדונו שלא להניח תפילין בימים אלו. לגבי חול המועד, יש שהניחו (למשל בחלק מקהילות אשכנז בחוץ לארץ בברכה בלחש), ברם בארץ ישראל התקבל מנהג האר"י והשולחן ערוך שחול המועד הוא גם בגדר "אות" ואין מניחים בו כלל.',
      priority: 1,
    });
  }

  if (!context.tachanunAllowed) {
    items.push({
      id: 'no-tachanun',
      title: 'לא אומרים תחנון',
      summary: 'ביום זה לא אומרים תחנון בתפילה.',
      contentHe: 'התחנון, הידוע גם בשם "נפילת אפים", הוא חלק מתפילת שחרית ומנחה המשקף אבלות וחרטה צער על חורבן המקדש וחטאינו. בימי שמחה (חגים, חול המועד, ראשי חודשים, ימי חג ספציפיים כגון חנוכה, פורים, פורים קטן, וכו\'), וכן כאשר נוכח חתן או אבי הבן ביום מילתו, מדלגים עליו כדי שלא לפגום באווירת השמחה.',
      priority: 2,
    });
  }

  if (context.isErevShabbat) {
    items.push({
      id: 'erev-shabbat',
      title: 'הכנות לשבת',
      summary: 'יש להכין את צרכי השבת מבעוד מועד. הדלקת נרות לפני השקיעה.',
      contentHe: 'מצווה מהתורה להתכונן לשבת, כפי גדלותו של אדם שעומד לקבל פני מלך. נוהגים לרחוץ פנים ידיים ורגליים במים חמים, לגזוז ציפורניים שמראשן כוונת טהרה, ולהכין תבשילים טובים. יש להקפיד לסיים את מלאכות החול מוקדם ככל האפשר ולהדליק נרות שבת תוך כדי ברכה באזור השקיעה או לפני כן כדי לקבל את השבת באור מבעוד מועד.',
      priority: 1,
    });
  }

  if (context.sefiraDayHe) {
    items.push({
      id: 'sefirat-haomer',
      title: `ספירת העומר — ${context.sefiraDayHe}`,
      summary: 'בימי הספירה נוהגים מנהגי אבלות: לא מסתפרים, לא עורכים חתונות (לפי מנהגים).',
      contentHe: 'ספירת העומר היא מצוות עשה מהתורה המתבצעת מאסרו חג ראשון של פסח ועד לערב חג השבועות - שבעה שבועות תמימים. בתקופה זו (24,000 תלמידי רבי עקיבא שמתו) נוהגים במנהגי אבלות חלקיים: הימנעות מגילוח ותספורת, אי עריכת נישואין, והימנעות מקניית בגדים חדשים לחלוטין (למנהגים מסוימים או רק בחלק מהתקופה - עד לג\' בעומר או שבועות).',
      priority: 2,
    });
  }

  // Always add a general daily item
  items.push({
    id: 'morning-brachot',
    title: 'ברכות השחר',
    summary: 'כל יום יש לברך את ברכות השחר כראוי, בשם ומלכות.',
    contentHe: 'ברכות השחר נתקנו על ההנאות הרגילות שמקבל אדם כשהוא קם משנתו: שהחזיר לו את נשמתו (אלוהי נשמה), שפוקח לעיוורים, מתיר אסורים, וכו\'. סדר הברכות מסמל את תהליך הקימה וההתארגנות הפיזית. מאחר שתקנתן אישית, אפילו אם הקדים האדם ולבש ולמד בלילה לאחר שנתו, חייב לברכן על סדר היום. ויש אומרים אותן בסמוך לתפילת שחרית ברוב עם בבית הכנסת כדי לענות אמן בציבור.',
    priority: 3,
  });

  return items.sort((a, b) => a.priority - b.priority);
}
